package help.nic.model;

public class studentcomp {
private String enrollmentno;

private String compid;
private String compdes;
private String dateofcomp;
private String action;
private String sol;
private String feedback;
public String getFeedback() {
	return feedback;
}
public void setFeedback(String feedback) {
	this.feedback = feedback;
}
public String getSol() {
	return sol;
}
public void setSol(String sol) {
	this.sol = sol;
}
public String getEnrollmentno() {
	return enrollmentno;
}
public void setEnrollmentno(String enrollmentno) {
	this.enrollmentno = enrollmentno;
}


public String getCompid() {
	return compid;
}
public void setCompid(String compid) {
	this.compid = compid;
}
public String getCompdes() {
	return compdes;
}
public void setCompdes(String compdes) {
	this.compdes = compdes;
}
public String getDateofcomp() {
	return dateofcomp;
}
public void setDateofcomp(String dateofcomp) {
	this.dateofcomp = dateofcomp;
}
public String getAction() {
	return action;
}
public void setAction(String action) {
	this.action = action;
}
	
}
